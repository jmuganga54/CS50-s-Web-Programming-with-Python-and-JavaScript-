#Creating a for loop
for i in [0, 1, 2, 3, 4, 5]:
    print(i)

#Creating a for loop using range function
for i in range(6):
    print(i)

#looping through a list
names = ["Joseph","Brian","Anna","Peter"]
for name in names:
    print(name)

#looping
name = "Harry"
for character in name:
    print(character)